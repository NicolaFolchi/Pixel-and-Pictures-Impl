package a5;

public class Coordinate {

  private int x;
  private int y;
  
  /*
   * Creates a coordinate interval
   */
  public Coordinate(int x, int y) {
    this.x = x;
    this.y = y;
  }
  
  /*
   * Getter for x
   * Returns x
   */
  public int getX() {
    return x;
  }
  
  /*
   * Getter for y
   * Returns y
   */
  public int getY() {
    return y;
  }
}
